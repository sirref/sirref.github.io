export class LeadboardEntry {
    constructor(id, rank, name, score, kills, deaths, assists, healing, damage) {
        this.id = id;
        this.rank = rank;
        this.name = name;
        this.score = score;
        this.kills = kills;
        this.deaths = deaths;
        this.assists = assists;
        this.healing = healing;
        this.damage = damage;
    }
}
