class LeaderboardEntry {
    constructor(id, warid, rank, name, score, kills, deaths, assists, healing, damage, team) {
        this.id = id;
        this.warid = warid;
        this.rank = rank;
        this.name = name;
        this.score = score;
        this.kills = kills;
        this.deaths = deaths;
        this.assists = assists;
        this.healing = healing;
        this.damage = damage;
        this.team = team;
    }
}
